    <script>
  function toggleFaq(index) {
    const allAnswers = document.querySelectorAll('#accordion > div > div:nth-child(2)');
    const selectedAnswer = allAnswers[index];
    const isOpen = !selectedAnswer.classList.contains('hidden');

    
    allAnswers.forEach((el, i) => {
      if (i !== index) el.classList.add('hidden');
    });

    // Toggle the selected one
    if (isOpen) {
      selectedAnswer.classList.add('hidden');
    } else {
      selectedAnswer.classList.remove('hidden');
    }
  }
</script>



